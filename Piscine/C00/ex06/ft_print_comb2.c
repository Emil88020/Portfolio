#include <unistd.h>

void	check_comb2(char *n)
{
	if (n[1] < '9')
	{
		n[1]++;
	}
	else
	{
		n[0]++;
		n[1] = '0';
	}
	n[3] = n[0];
	if (n[1] + 1 > '9')
	{
		n[3] = n[0] + 1;
		n[4] = '0';
	}
	else
	{
		n[4] = n[1] + 1;
	}
}

void	check_comb(char *n)
{
	if (n[4] < '9')
	{
		n[4]++;
	}
	else
	{
		if (n[3] < '9')
		{
			n[3]++;
			n[4] = '0';
		}
		else
		{
			check_comb2(n);
		}
	}
}

void	ft_print_comb2(void)
{
	char	n[5];
	char	cs[2];

	n[0] = '0';
	n[1] = '0';
	n[2] = ' ';
	n[3] = '0';
	n[4] = '1';
	cs[0] = ',';
	cs[1] = ' ';
	while (n[0] <= '9')
	{
		write(1, n, 5);
		if ((n[0] == '9' && n[1] == '8') && (n[3] == '9' && n[4] == '9'))
		{
			break ;
		}
		write(1, cs, 2);
		check_comb(n);
	}
}
